/* ============================================================
   2) RELATIONAL OPERATORS: ==  !=  >  <  >=  <=
   Notes:
   - Output is 1 (true) or 0 (false)
   ============================================================ */
#include <iostream>
using namespace std;

int main() {
    int a = 6, b = 4;

    cout << "a == b : " << (a == b) << endl;
    cout << "a != b : " << (a != b) << endl;
    cout << "a >  b : " << (a > b) << endl;
    cout << "a <  b : " << (a < b) << endl;
    cout << "a >= b : " << (a >= b) << endl;
    cout << "a <= b : " << (a <= b) << endl;

    return 0;
}
